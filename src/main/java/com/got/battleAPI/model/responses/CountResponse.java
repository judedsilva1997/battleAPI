package com.got.battleAPI.model.responses;

public class CountResponse {

	Integer count;

	public CountResponse() {
	}

	public CountResponse(Integer count) {
		super();
		this.count = count;
	}

	public Integer getCount() {
		return count;
	}

	public void setCount(Integer count) {
		this.count = count;
	}

}
